package Day4;

public class Math_Class {
	public static void main(String[] args) {
		System.out.println("Absolute value of -7:" + Math.abs(-7));
		System.out.println("Result of 3 power 7:"+ Math.pow(3, 7));
		System.out.println("Square root of 48:"+ Math.sqrt(49));
		System.out.println("Ramdom value:" + Math.random());
		System.out.println("Min value (100,50):" + Math.min(100, 50));
		System.out.println("Max value(40,90):" + Math.max(40, 90));
		System.out.println ( "Round (3.6):"+Math.round(3.6));
		System.out.println("Floor(3.8):" + Math.floor(3.8));
		System.out.println("Ceil(3.1):" + Math.ceil(3.1));
		System.out.println("Sin(30)" + Math.sin(30));
		System.out.println("Log10(10)" + Math.log10(10));
	}
}
